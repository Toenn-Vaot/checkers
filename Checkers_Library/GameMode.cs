
namespace Checkers_Library
{
    /// <summary>
    /// This enum lists the available play modes in the game
    /// </summary>
    public enum GameMode
    {
        /// <summary>
        /// Not defined
        /// </summary>
        None = 0,
        /// <summary>
        /// Board of 12x12 cases
        /// </summary>
        Mode12X12 = 12,
        /// <summary>
        /// Board of 10x10 cases
        /// </summary>
        Mode10X10 = 10,
        /// <summary>
        /// Board of 8x8 cases
        /// </summary>
        Mode8X8 = 8,
    }
}
